    /**
     * Método que agrega una materia a la lista de materias
     * @param evt 
     */
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        if (!txtCodigo.getText().isEmpty() && !txtNombreMateria.getText().isEmpty() 
                && !txtAnio.getText().isEmpty()){
            try{
                    String codigoMateria = txtCodigo.getText().trim();
                    String nombre = txtNombreMateria.getText().trim();
                    boolean existeMateria = GestionEscuela.existeMateria(codigoMateria);
                    if(!existeMateria){
                        int anios = Integer.parseInt(txtAnio.getText().trim());
                        
                        Materia unaMateria = new Materia(codigoMateria,nombre,anios);
                        GestionEscuela.agregarMateria(unaMateria);
                        JOptionPane.showMessageDialog(null, "Materia Agregado Correctamente");
                        txtCodigo.setText("");
                        txtNombreMateria.setText("");
                        txtAnio.setText("");
                        
                    }else{
                        JOptionPane.showMessageDialog(null, "Ya existe materia con ese código");
                    }
                }catch(NumberFormatException | HeadlessException ex){
                    JOptionPane.showMessageDialog(null, "No cumple el formato " + ex.getMessage());
                }
            
        }else{
            JOptionPane.showMessageDialog(null, "Faltan datos, verificar..");
        }        
    }//GEN-LAST:event_btnAgregarActionPerformed

