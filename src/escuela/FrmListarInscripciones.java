    public void listarInscripciones(){
        int cantidadInscripciones = GestionEscuela.listaInscripciones.size();
        DefaultTableModel modelo3 = (DefaultTableModel) tblInscripciones.getModel();
        tblInscripciones.setModel(modelo3);
        String[] datos = new String[6];
        for(int i=0;i<cantidadInscripciones;i++){
            datos[0]=GestionEscuela.listaInscripciones.get(i).getUnAlumno().getIdentificacion();
            datos[1]=GestionEscuela.listaInscripciones.get(i).getUnAlumno().getNombre();
            datos[2]=GestionEscuela.listaInscripciones.get(i).getUnAlumno().getApellido();            
            datos[3]=GestionEscuela.listaInscripciones.get(i).getUnaMateria().getNombre();
            datos[4]=Integer.toString(GestionEscuela.listaInscripciones.get(i).getUnaMateria().getAnio());
            datos[5]=GestionEscuela.listaInscripciones.get(i).getFecha().toString();
            modelo3.addRow(datos);            
        }      
    }

