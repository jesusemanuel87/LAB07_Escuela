    public void listarMaterias(){
        int cantidad = GestionEscuela.listaMaterias.size();
        DefaultTableModel modelo = (DefaultTableModel) tblMaterias.getModel();
        tblMaterias.setModel(modelo);
        String[] datos = new String[3];
        for(int i=0;i<cantidad;i++){
            datos[0]=GestionEscuela.listaMaterias.get(i).getCodigo();
            datos[1]=GestionEscuela.listaMaterias.get(i).getNombre();
            datos[2]=Integer.toString(GestionEscuela.listaMaterias.get(i).getAnio());
            modelo.addRow(datos);           
        }
        
    }

